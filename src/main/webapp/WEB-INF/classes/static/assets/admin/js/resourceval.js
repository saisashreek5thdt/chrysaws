

               function fileTypeValidation() {

         var fileInput = document.getElementById('customFile');

                                       var fileInputType = document.getElementById('filetype').value;

                                var filePath = fileInput.value;

                                // Allowing file type

                                if(fileInputType=="video")
                                {
                             var  allowedExtensions =
                                        /(\.avi|\.mp4|\.mpeg|\.mov|\.flv|\.wmv|\.gif)$/i;

                                                 if (!allowedExtensions.exec(filePath)) {
                                   alert('Invalid file type!Accepted Types:.avi,.mp4,.mpeg,.flv,.wmv,.flv,.mov ');
                                                fileInput.value = '';
                                               return false;
                                                             }
                              }

                                 if(fileInputType=="word")
                                    {
                                          var  allowedExtensions =  /(\.doc|\.docx)$/i;
                  if (!allowedExtensions.exec(filePath)) {
           alert('Invalid file type!Accepted Types:.doc,.docx ');
                       fileInput.value = '';
                 return false;
                               }
                                          }

                         if(fileInputType=="ppt")
                           {
                              var  allowedExtensions =  /(\.ppt|\.pptx)$/i;
                       if (!allowedExtensions.exec(filePath)) {
                                alert('Invalid file type!Accepted Types:.ppt ');
                                               fileInput.value = '';
                                      return false;
                                   }
                            }


                      if(fileInputType=="pdf")
                       {
                              var  allowedExtensions =  /(\.pdf)$/i;
                            if (!allowedExtensions.exec(filePath)) {
                                alert('Invalid file type!Accepted Types:.pdf ');
                                               fileInput.value = '';
                                      return false;
                                   }
                            }




                                }

















